class Calculator():
    def add_numbers(a, b):
    	return a + b
    def subtract_numbers(a, b):
    	return a - b
    def multiply_numbers(a, b):
        return a * b
    def divide_numbers(a, b):
        return a / b
    def opposite(a):
        return -a
    def modulo(a, b):
        return a % b
